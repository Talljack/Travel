      },
    },
  },
  data() {
    return {
      swiperOption: {
        pagination: '.swiper-pagination',
        paginationType: 'fraction',
        observer: true,
        observeParents: true,
      },
    };
  },
  methods: {
    handleSwiperClose() {
      this.$emit('close');
    },
  },
};
</script>
